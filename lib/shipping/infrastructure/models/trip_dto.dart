import 'package:fasta/shipping/domain/entity/delivery.dart';

class TripDTO extends Trip {
  TripDTO(
      {required String from,
      required String to,
      required String tripId,
      required String kilometer,
      required String distance,
      required String price,
      required String driverName,
      required String phoneNumber,
      required String email,
      required String vechicleNumber,
      required String createAt,
      required String status,
      required String senderName,
      required String pickupTime,
      required int deliveryPriority,
      required String recieversName,
      required String receiversNumber,
      required String itemName,
      required String qty,
      required String value,
      required String weight,
      required String state,
      required String descriptionDetails,
      required String image,
      required String category,
      required String nameFrom,
      required String nameTo,
      required String vechicleType,
      required String rating,
      required String paymentMethod,
      required int id})
      : super(
            from: from,
            to: to,
            tripId: tripId,
            kilometer: kilometer,
            distance: distance,
            price: price,
            driverName: driverName,
            phoneNumber: phoneNumber,
            email: email,
            vechicleNumber: vechicleNumber,
            createAt: createAt,
            status: status,
            rider: driverName,
            departureTime: '',
            itemName: itemName,
            rating: rating,
            paymentMethod: paymentMethod,
            arrivalTime: '',
            id: id);
  factory TripDTO.fromJson(Map<String, dynamic> json) {
    return TripDTO(
        from: json['pickup_address'],
        to: json['delivery_point'],
        tripId: json['order_num'],
        kilometer: json['kilometer'] ?? '2km',
        distance: json['distance'] ?? '2km',
        price: json['price'] ?? '700 NGN',
        driverName: json['rider'],
        phoneNumber: json['phone_num'],
        email: json['email'],
        vechicleNumber: json['vehicle'],
        createAt: json['created_at'],
        status: json['status'],
        id: json['id'],
        category: json['category'],
        deliveryPriority: json['delivery_priority'],
        descriptionDetails: json['description_details'],
        image: json['image'],
        itemName: json['item_name'],
        pickupTime: json['pickup_time'],
        nameTo: json['name_to'],
        qty: json['qty'],
        receiversNumber: json['receivers_number'],
        rating: json['rating'],
        recieversName: json['receivers_name'],
        state: json['state'],
        nameFrom: json['name_from'],
        vechicleType: json['vehicle_type'],
        value: json['value'],
        weight: json['weight'],
        senderName: json['sender_name'],
        paymentMethod: 'Wallet');
  }
}
